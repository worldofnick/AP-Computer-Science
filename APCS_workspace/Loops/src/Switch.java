
public class Switch {

	public static void main(String[] args) {
		
		int testscore = 60;
		
		switch (testscore){
		case 60: 
			System.out.println("Study up bro.");
			break;
			case 70: 
				System.out.println("You might want to study up next time.");;
			break;
			case 80: 
				System.out.println("Slightly better");
			break;
			case 90: 
				System.out.println("Good job!");
			break;
			case 100: 
				System.out.println("amazing");
			break;
		}

	}

}
